				</h2>
			

			
				<p><img src="/static/img-blog/follow-and-unfollow.png" class="blog-img"/></p>
			

			
				<p>
With our new feature, Follow & Unfollow at the same time, your Activity can automatically switch between following and unfollowing.
</p>
<p><strong>How does it work?</strong></p>
<p>
Make sure you have enabled both the Follow and Unfollow actions in your Activity. <b>It’s also very important to choose “Unfollow source: Instagress” for normal (not reverse) unfollow order and better results.</b> Then find and adjust your Follow & Unfollow cycles settings in appropriate categories. You can set the number of users you’d like Instagress to follow for you before switching to unfollowing. Your Activity will continue to switch back and forth between Follow and Unfollow cycles infinitely or until your Follow/Unfollow auto-stop counter is hit.
</p>
<p><strong>Recommended cycles</strong></p>
<p>
We recommend you to set not less than 500 for each cycle, what means that you will unfollow users after one day, but you can find your own cycle amounts that better suit your needs. For example, if you run your Activity on Normal speed (~20 Follow actions per hour) and you want to unfollow users after 3 days then you need to set your Follow cycle to 1440 (20 * 24 * 3).
</p>
<p><strong>What happens when I hit a limit?</strong></p>
<p>
Your Activity will automatically switch to the Unfollow cycle if your account hits an Instagram follow limit, which would prohibit you from following more users. Conversely, when there are no more users to unfollow, your Activity will automatically switch to the Follow cycle.
</p>
<p><strong>Share your feedback</strong></p>
<p>
We can’t wait to see how Follow & Unfollow cycles are working out for you. Please give us your feedback in the comments below.
</p>

				
			

			

			
